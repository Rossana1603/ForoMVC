            //app.UseFacebookAuthentication(
            //   appId: "796820797036265",
            //   appSecret: "9d52c7c3cadcc6922bd720df546cda0a");

            app.UseFacebookAuthentication(
               appId: "399726786842614",
               appSecret: "d0ae6132e58976cf1da8f50e2ef6b861");

            app.UseGoogleAuthentication(
